# =============================================================================
# Program   : adsl.R
# Study     : CDISC Pilot Study (two-arm parallel group)
# Purpose   : Derive ADaM Subject-Level Analysis Dataset (ADSL)
# Source    : pharmaversesdtm
# Standard  : CDISC ADaM IG v1.3
# =============================================================================

library(admiral)
library(dplyr)
library(lubridate)
library(pharmaversesdtm)
library(Hmisc)

# -----------------------------------------------------------------------------
# Step 1 — Load SDTM domains
# -----------------------------------------------------------------------------

dm  <- pharmaversesdtm::dm
ex  <- pharmaversesdtm::ex
ds  <- pharmaversesdtm::ds
ae  <- pharmaversesdtm::ae   # used for LSTALVDT
lb  <- pharmaversesdtm::lb   # used for LSTALVDT

# -----------------------------------------------------------------------------
# Step 2 — Subject spine from DM
# One record per USUBJID is enforced; all downstream merges must preserve this.
# -----------------------------------------------------------------------------

adsl <- dm |>
  select(
    STUDYID, USUBJID, SUBJID, SITEID,
    AGE, AGEU, SEX, RACE, ETHNIC, COUNTRY,
    ARM, ARMCD, ACTARM, ACTARMCD,
    DMDTC, RFSTDTC, RFENDTC,
    DTHDTC, DTHFL
  )

# -----------------------------------------------------------------------------
# Step 3 — Treatment datetimes from EX
# TRTSDTM: first dose datetime  (EXSTDTC)
# TRTEDTM: last  dose datetime  (EXENDTC)
# Imputation flags: TRTSTMF, TRTETMF (generated by derive_vars_dtm)
# -----------------------------------------------------------------------------

# Derive datetime variables on EX before merging
ex_dt <- ex |>
  derive_vars_dtm(
    dtc              = EXSTDTC,
    new_vars_prefix  = "EXST",
    date_imputation  = "first",
    time_imputation  = "first",
    flag_imputation  = "auto"
  ) |>
  derive_vars_dtm(
    dtc              = EXENDTC,
    new_vars_prefix  = "EXEN",
    date_imputation  = "last",
    time_imputation  = "last",
    flag_imputation  = "auto"
  )

# TRTSDTM / TRTSTMF — first dose
adsl <- adsl |>
  derive_vars_merged(
    dataset_add = ex_dt,
    by_vars     = exprs(STUDYID, USUBJID),
    new_vars    = exprs(TRTSDTM = EXSTDTM, TRTSTMF = EXSTTMF),
    order       = exprs(EXSTDTM),
    mode        = "first",
    filter_add  = !is.na(EXSTDTM)
  ) |>
  # TRTEDTM / TRTETMF — last dose
  derive_vars_merged(
    dataset_add = ex_dt,
    by_vars     = exprs(STUDYID, USUBJID),
    new_vars    = exprs(TRTEDTM = EXENDTM, TRTETMF = EXENTMF),
    order       = exprs(EXENDTM),
    mode        = "last",
    filter_add  = !is.na(EXENDTM)
  ) |>
  mutate(
    # Date-only versions of treatment start/end
    TRTSDT = as.Date(TRTSDTM),
    TRTEDT = as.Date(TRTEDTM)
  )

# TRTDURD: total treatment duration in days (inclusive)
# REVIEW: confirm whether duration is inclusive (TRTEDT - TRTSDT + 1) or exclusive per protocol
adsl <- adsl |>
  mutate(
    TRTDURD = as.integer(TRTEDT - TRTSDT) + 1L
  )

# -----------------------------------------------------------------------------
# Step 4 — Planned and actual treatment labels and numeric codes
# TRT01P:  planned treatment (DM.ARM)
# TRT01A:  actual  treatment (DM.ACTARM)
# TRT01PN/TRT01AN: numeric codes per ADaM spec
#   Xanomeline High Dose = 3, Xanomeline Low Dose = 2, Placebo = 1
# REVIEW: confirm numeric code mapping against randomisation schedule
# -----------------------------------------------------------------------------

adsl <- adsl |>
  mutate(
    TRT01P = ARM,
    TRT01A = ACTARM,
    TRT01PN = case_when(
      TRT01P == "Xanomeline High Dose" ~ 3L,
      TRT01P == "Xanomeline Low Dose"  ~ 2L,
      TRT01P == "Placebo"              ~ 1L,
      TRUE                             ~ NA_integer_
    ),
    TRT01AN = case_when(
      TRT01A == "Xanomeline High Dose" ~ 3L,
      TRT01A == "Xanomeline Low Dose"  ~ 2L,
      TRT01A == "Placebo"              ~ 1L,
      TRUE                             ~ NA_integer_
    )
  )

# -----------------------------------------------------------------------------
# Step 5 — Randomisation and reference dates
# RANDDT: date of randomisation from DM.DMDTC
# RFSTDT: reference start date (DM.RFSTDTC)
# RFENDT: reference end   date (DM.RFENDTC)
# -----------------------------------------------------------------------------

adsl <- adsl |>
  derive_vars_dt(
    dtc             = RFSTDTC,
    new_vars_prefix = "RFST",
    date_imputation = "none"
  ) |>
  derive_vars_dt(
    dtc             = RFENDTC,
    new_vars_prefix = "RFEND",
    date_imputation = "none"
  ) |>
  mutate(
    RANDDT = as.Date(DMDTC)
  )

# -----------------------------------------------------------------------------
# Step 6 — Death variables
# DTHFL: from DM (already loaded); recode to CDISC "Y"/NA convention
# DTHDT: from DM.DTHDTC
# REVIEW: confirm DTHFL source — DM.DTHFL should already be Y/NA in CDISC SDTM
# -----------------------------------------------------------------------------

adsl <- adsl |>
  derive_vars_dt(
    dtc             = DTHDTC,
    new_vars_prefix = "DTH",
    date_imputation = "none"
  ) |>
  mutate(
    # Ensure CDISC flag convention: "Y" or NA
    DTHFL = if_else(DTHFL == "Y", "Y", NA_character_)
  )

# -----------------------------------------------------------------------------
# Step 7 — Disposition (EOSSTT, DCSREAS, EOSDT)
# Filter DS to the primary disposition event per CDISC convention.
# REVIEW: confirm DSCAT/DSDECOD values in the study DS domain match expected
#         CDISC codes; adjust filter if DSCAT label differs.
# -----------------------------------------------------------------------------

ds_eos <- ds |>
  filter(DSCAT == "DISPOSITION EVENT") |>
  derive_vars_dt(
    dtc             = DSDTC,
    new_vars_prefix = "DS",
    date_imputation = "none"
  )

adsl <- adsl |>
  # EOSSTT: study completion status
  # REVIEW: verify "COMPLETED" / "DISCONTINUED" are the protocol-specified values
  derive_vars_merged(
    dataset_add = ds_eos,
    by_vars     = exprs(STUDYID, USUBJID),
    new_vars    = exprs(
      EOSSTT  = DSDECOD,
      DCSREAS = DSDECOD,
      EOSDT   = DSDT
    ),
    order      = exprs(DSDT),
    mode       = "last",
    filter_add = DSCAT == "DISPOSITION EVENT"
  ) |>
  mutate(
    # EOSSTT: collapse to COMPLETED / DISCONTINUED per protocol
    EOSSTT = case_when(
      EOSSTT == "COMPLETED"    ~ "COMPLETED",
      !is.na(EOSSTT)           ~ "DISCONTINUED",
      TRUE                     ~ NA_character_
    ),
    # DCSREAS: NA for completers per ADaM convention
    DCSREAS = if_else(EOSSTT == "COMPLETED", NA_character_, DCSREAS)
  )

# -----------------------------------------------------------------------------
# Step 8 — Baseline demographics / age groupings
# Cut-points per ADaM spec: <65 / 65-80 / >80
# REVIEW: confirm cut-points against the SAP — these are protocol-specific
# -----------------------------------------------------------------------------

adsl <- adsl |>
  mutate(
    AGEGR1 = case_when(
      AGE < 65             ~ "<65",
      AGE >= 65 & AGE <= 80 ~ "65-80",
      AGE > 80             ~ ">80",
      TRUE                 ~ NA_character_
    ),
    AGEGR1N = case_when(
      AGEGR1 == "<65"   ~ 1L,
      AGEGR1 == "65-80" ~ 2L,
      AGEGR1 == ">80"   ~ 3L,
      TRUE              ~ NA_integer_
    )
  )

# -----------------------------------------------------------------------------
# Step 9 — Population flags
# SAFFL: received at least one dose (EXDOSE > 0 or EXTRT contains "PLACEBO")
#        with a non-missing EXSTDTC
# ITTFL: randomised — ARMCD != "Scrnfail" and ARM != "Screen Failure"
# REVIEW: verify population flag logic against the SAP/protocol before use
# -----------------------------------------------------------------------------

# SAFFL — at least one qualifying exposure record
adsl <- adsl |>
  derive_var_merged_exist_flag(
    dataset_add = ex,
    by_vars     = exprs(STUDYID, USUBJID),
    new_var     = SAFFL,
    # REVIEW: EXDOSE > 0 covers active treatment; grepl("PLACEBO") covers placebo
    #         Remove or adjust the placebo condition if EXDOSE is always > 0 for placebo
    condition   = !is.na(EXSTDTC) & (EXDOSE > 0 | grepl("PLACEBO", EXTRT, ignore.case = TRUE))
  )

# ITTFL — randomised subjects
adsl <- adsl |>
  mutate(
    # REVIEW: confirm ITT definition with statistician — uses ARMCD and ARM per protocol
    ITTFL = if_else(
      ARMCD != "Scrnfail" & ARM != "Screen Failure",
      "Y",
      NA_character_
    )
  )

# -----------------------------------------------------------------------------
# Step 10 — Last known alive date (LSTALVDT)
# Sources (in descending priority): DTHDT, latest AE date, latest LB date, RFENDT
# REVIEW: confirm priority hierarchy with statistician; add additional sources
#         (e.g. VS, CM) if available and in scope
# -----------------------------------------------------------------------------

# Latest AE end / start date per subject
ae_last <- ae |>
  derive_vars_dt(
    dtc             = AEENDTC,
    new_vars_prefix = "AEEND",
    date_imputation = "last"
  ) |>
  derive_vars_dt(
    dtc             = AESTDTC,
    new_vars_prefix = "AEST",
    date_imputation = "first"
  ) |>
  mutate(AELASTDT = pmax(AEENDDT, AESTDT, na.rm = TRUE)) |>
  group_by(STUDYID, USUBJID) |>
  summarise(AE_LSTDT = max(AELASTDT, na.rm = TRUE), .groups = "drop") |>
  mutate(AE_LSTDT = if_else(is.infinite(AE_LSTDT), as.Date(NA), AE_LSTDT))

# Latest LB date per subject
lb_last <- lb |>
  derive_vars_dt(
    dtc             = LBDTC,
    new_vars_prefix = "LB",
    date_imputation = "first"
  ) |>
  group_by(STUDYID, USUBJID) |>
  summarise(LB_LSTDT = max(LBDT, na.rm = TRUE), .groups = "drop") |>
  mutate(LB_LSTDT = if_else(is.infinite(LB_LSTDT), as.Date(NA), LB_LSTDT))

adsl <- adsl |>
  left_join(ae_last, by = c("STUDYID", "USUBJID")) |>
  left_join(lb_last, by = c("STUDYID", "USUBJID")) |>
  mutate(
    LSTALVDT = pmax(DTHDT, AE_LSTDT, LB_LSTDT, RFENDDT, na.rm = TRUE)
  ) |>
  select(-AE_LSTDT, -LB_LSTDT)   # drop helper columns

# -----------------------------------------------------------------------------
# Step 11 — Final variable selection and ordering
# Keep only ADaM ADSL variables; drop SDTM working variables
# -----------------------------------------------------------------------------

adsl <- adsl |>
  select(
    # Identifiers
    STUDYID, USUBJID, SUBJID, SITEID,
    # Demographics
    AGE, AGEU, AGEGR1, AGEGR1N,
    SEX, RACE, ETHNIC, COUNTRY,
    # Treatment
    TRT01P, TRT01PN, TRT01A, TRT01AN,
    TRTSDTM, TRTSTMF, TRTEDTM, TRTETMF,
    TRTSDT, TRTEDT, TRTDURD,
    # Reference dates
    RFSTDT, RFENDDT, RANDDT,
    # Disposition
    EOSSTT, EOSDT, DCSREAS,
    # Death
    DTHFL, DTHDT,
    # Last alive
    LSTALVDT,
    # Population flags
    SAFFL, ITTFL
  )

# -----------------------------------------------------------------------------
# Step 12 — Quality assertions
# -----------------------------------------------------------------------------

# One record per USUBJID
stopifnot(
  "ADSL must have exactly one record per USUBJID" =
    nrow(adsl) == dplyr::n_distinct(adsl$USUBJID)
)

# Required variables present
required_vars <- c(
  "STUDYID", "USUBJID", "TRTSDT", "TRTEDT",
  "TRT01P", "TRT01A", "EOSSTT", "SAFFL", "ITTFL"
)
missing_vars <- setdiff(required_vars, names(adsl))
if (length(missing_vars) > 0) {
  stop("Missing required ADSL variables: ", paste(missing_vars, collapse = ", "))
}

message("ADSL derived successfully: ", nrow(adsl), " subjects, ",
        ncol(adsl), " variables.")

